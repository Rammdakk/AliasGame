import Foundation

enum HTTPMethod: String {
    case get = "GET" // Represents the GET HTTP method
    case post = "POST" // Represents the POST HTTP method
    case put = "PUT" // Represents the PUT HTTP method
    case delete = "DELETE" // Represents the DELETE HTTP method
}
